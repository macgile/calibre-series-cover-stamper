def normalize_language(value):
    """Return a supported language code with English fallback."""
    value = str(value or LANGUAGE_EN).lower()
    if value not in TRANSLATIONS:
        return LANGUAGE_EN
    return value


def tr(lang, key, **kwargs):
    """Return a localized string and apply optional format values."""
    lang = normalize_language(lang)
    template = TRANSLATIONS.get(lang, {}).get(key)
    if template is None:
        template = TRANSLATIONS[LANGUAGE_EN].get(key, key)
    if kwargs:
        try:
            return template.format(**kwargs)
        except Exception:
            return template
    return template
